"""Unit tests for app.handler.embeddings.loader.load_embedding_model.

Drives the loader from the on-disk tiny fixtures generated by
scripts/generate_tiny_bert_fixture.py and scripts/generate_tiny_nomic_fixture.py.
Also exercises the fail-loud config validator by mutating copies of those
fixtures into unsupported variants.
"""
import json
import shutil
import tempfile
from pathlib import Path

import pytest

from app.handler.embeddings.loader import (
    UnsupportedModelError,
    _remap_hf_to_internal,
    load_embedding_model,
)

FIXTURES = Path(__file__).resolve().parent / "fixtures"
BERT_FIXTURE = FIXTURES / "tiny_bert"
NOMIC_FIXTURE = FIXTURES / "tiny_nomic"


def test_loads_tiny_bert():
    """Vanilla BERT fixture loads with mean pooling and no matryoshka."""
    model, tokenizer, pooling_mode, matryoshka_dim = load_embedding_model(str(BERT_FIXTURE))
    assert model is not None
    assert tokenizer is not None
    assert pooling_mode == "mean"
    assert matryoshka_dim is None


def test_loads_tiny_nomic():
    """Nomic-style RoPE+SwiGLU fixture loads — covers SUPPORTED non-default variant."""
    model, tokenizer, pooling_mode, matryoshka_dim = load_embedding_model(str(NOMIC_FIXTURE))
    assert model is not None
    assert tokenizer is not None
    assert pooling_mode == "mean"


def _copy_fixture(src: Path, dst: Path) -> None:
    """Copy a fixture directory into dst (which must already exist or be empty)."""
    shutil.copytree(src, dst, dirs_exist_ok=True)


def test_unsupported_activation_errors():
    """An unrecognized hidden_act value must raise with a clear message."""
    with tempfile.TemporaryDirectory() as tmp:
        tmp_dir = Path(tmp)
        _copy_fixture(BERT_FIXTURE, tmp_dir)
        cfg_path = tmp_dir / "config.json"
        cfg = json.loads(cfg_path.read_text())
        cfg["hidden_act"] = "gelu_new"
        cfg_path.write_text(json.dumps(cfg))
        with pytest.raises(UnsupportedModelError, match="hidden_act='gelu_new'"):
            load_embedding_model(str(tmp_dir))


